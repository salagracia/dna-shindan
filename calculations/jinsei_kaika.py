def calculate_jinsei_kaika(answers: dict) -> dict:
    """20問の回答からタイプ判定
    answers: {"K1": "5", "K2": "4", ...} （文字列の数値1-5）
    """
    scores = {t: 0.0 for t in "ABCDEFGH"}

    for q in QUESTIONS:
        ans = answers.get(q["id"])
        if ans is None:
            continue
        try:
            score_val = int(ans)
        except (ValueError, TypeError):
            continue

        if score_val < 1 or score_val > 5:
            continue

        # 5段階を-2〜+2に正規化（3は中立=0）
        normalized = score_val - 3  # 5→2, 4→1, 3→0, 2→-1, 1→-2

        for type_code, weight in q["weights"].items():
            scores[type_code] += normalized * weight

    # 最高スコアのタイプ
    top_type = max(scores, key=scores.get)
    second_type = sorted(scores, key=scores.get, reverse=True)[1]

    profile = TYPE_PROFILES.get(top_type, {})
    second_profile = TYPE_PROFILES.get(second_type, {})

    return {
        "type": top_type,
        "name": profile.get("name", ""),
        "tagline": profile.get("tagline", ""),
        "summary": profile.get("summary", ""),
        "strengths": profile.get("strengths", []),
        "growth": profile.get("growth", ""),
        "fortune_strategy": profile.get("fortune_strategy", ""),
        "best_partner": profile.get("best_partner", ""),
        "for_50s": profile.get("for_50s", ""),
        "scores": scores,
        "second_type": second_type,
        "second_name": second_profile.get("name", ""),
    }


